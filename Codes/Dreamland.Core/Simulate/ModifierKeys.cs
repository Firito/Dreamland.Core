namespace Dreamland.Core.Simulate
{
    /// <summary>
    ///    指定修改键集。
    /// </summary>
    public enum ModifierKeys
    {
        /// <summary>
        ///     没有按下任何修饰符。
        /// </summary>
        None=0,
        /// <summary>
        ///     Alt 键。
        /// </summary>
        Alt=1,
        /// <summary>
        ///     CTRL 键。
        /// </summary>
        Control=2,
        /// <summary>
        ///     Shift 键。
        /// </summary>
        Shift=4,
        /// <summary>
        ///     Windows 徽标键。
        /// </summary>
        Windows=8,
    }
}
